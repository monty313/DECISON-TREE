def write_mql5_ea(t: TreeArrays, path: str, cfg, timeframes):
    tf_enum = ",".join(_TF_ENUM[tf] for tf in timeframes)
    sess = cfg.trade_sessions_utc[0]
    inputs = f"""input double InpRiskPctPerTrade = {cfg.risk_per_trade_pct*100:g};   // % equity risked / trade (0.01 = 0.01%)
input int    InpMaxTradesPerDay = {cfg.max_trades_per_day};
input double InpMaxDailyLossPct = {cfg.max_daily_loss_pct*100:g};
input double InpMaxTotalLossPct = {cfg.max_total_loss_pct*100:g};
input double InpDailyStopPct    = {cfg.daily_stop_pct*100:g};
input double InpDailyLockPct    = {cfg.daily_lock_profit_pct*100:g};
input double InpTotalStopPct    = {cfg.total_stop_pct*100:g};
input int    InpAtrPeriod       = {cfg.atr_period};
input double InpSlAtrMult       = {cfg.sl_atr_mult:g};
input double InpTpAtrMult       = {cfg.tp_atr_mult:g};
input int    InpMaxHoldBars     = {cfg.max_hold_bars};
input int    InpSessStartMin    = {sess[0]*60+sess[1]};   // UTC minutes
input int    InpSessEndMin      = {sess[2]*60+sess[3]};
input int    InpBrokerUtcOffset = {cfg.broker_utc_offset};
input bool   InpSwingMode       = {str(cfg.swing_mode).lower()};
input ulong  InpMagic           = 16016;
input int    InpSlippagePts     = {int(cfg.slippage_points)};"""
    ea = _EA_TEMPLATE
    ea = ea.replace("/*__INPUTS__*/", inputs)
    ea = ea.replace("/*__TF_ENUM__*/", tf_enum)
    ea = ea.replace("/*__NTF__*/", str(len(timeframes)))
    ea = ea.replace("/*__HANDLES_DECL__*/", _HANDLE_DECL)
    ea = ea.replace("/*__INIT_HANDLES__*/", _mql_init_handles(len(timeframes)))
    ea = ea.replace("/*__FEATURES__*/", mql_feature_lines(timeframes))
    ea = ea.replace("/*__EVALUATE_TREE__*/", export_mql5(t, "EvaluateTree"))
    ea = ea.replace("/*__NFEAT__*/", str(len(t.feature_names)))
    with open(path, "w") as f:
        f.write(ea)
    return path


