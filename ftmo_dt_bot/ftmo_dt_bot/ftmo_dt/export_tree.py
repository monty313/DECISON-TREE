"""export_tree.py - freeze a TreeArrays into pure-Python and MQL5 if/else.

A decision tree is just a chain of `if x[f] <= thr` tests, so it transpiles to
zero-dependency code that runs in the RL hot loop, the live Python bot, and the
MQL5 EA -- with NO sklearn/pandas/numpy at evaluation time. We also emit golden
(input -> label) rows so a test can prove the frozen code matches the trainer and
the two surfaces can never silently drift (CLAUDE.md section 5).
"""
from __future__ import annotations
import numpy as np
from .tree_model import TreeArrays, LEAF
from .feature_spec import FEATURES


def _py_body(t: TreeArrays, node, indent):
    pad = "    " * indent
    if t.children_left[node] == LEAF:
        return f"{pad}return {t._leaf_label(node)}\n"
    f = int(t.feature[node]); thr = float(t.threshold[node])
    s = f"{pad}if x[{f}] <= {thr!r}:  # {t.feature_names[f]}\n"
    s += _py_body(t, int(t.children_left[node]), indent + 1)
    s += f"{pad}else:\n"
    s += _py_body(t, int(t.children_right[node]), indent + 1)
    return s


def export_python(t: TreeArrays, func_name="evaluate_tree") -> str:
    hdr = (f"def {func_name}(x):\n"
           f"    # Frozen decision tree -> +1 (long) / -1 (short) / 0 (flat).\n"
           f"    # x is a list[float] in FEATURES order; caller must ensure all finite.\n")
    return hdr + _py_body(t, 0, 1)


def _mql_body(t: TreeArrays, node, indent):
    pad = "   " * indent
    if t.children_left[node] == LEAF:
        return f"{pad}return({t._leaf_label(node)});\n"
    f = int(t.feature[node]); thr = float(t.threshold[node])
    s = f"{pad}if(x[{f}] <= {thr!r}) // {t.feature_names[f]}\n{pad}{{\n"
    s += _mql_body(t, int(t.children_left[node]), indent + 1)
    s += f"{pad}}}\n{pad}else\n{pad}{{\n"
    s += _mql_body(t, int(t.children_right[node]), indent + 1)
    s += f"{pad}}}\n"
    return s


def export_mql5(t: TreeArrays, func_name="EvaluateTree") -> str:
    hdr = (f"// Frozen decision tree -> +1 long / -1 short / 0 flat.\n"
           f"int {func_name}(const double &x[])\n{{\n")
    return hdr + _mql_body(t, 0, 1) + "}\n"


def golden_rows(t: TreeArrays, n=200, seed=0):
    rng = np.random.default_rng(seed)
    X = rng.normal(0, 3, size=(n, len(t.feature_names)))
    y = t.predict(X)
    return X.tolist(), y.tolist()


def write_python_module(t: TreeArrays, path, symbol="POOLED", meta=None):
    src = export_python(t)
    gx, gy = golden_rows(t, 64, seed=1)
    with open(path, "w") as f:
        f.write('"""AUTO-GENERATED frozen decision tree. Do not edit by hand."""\n')
        f.write(f"SYMBOL = {symbol!r}\n")
        f.write(f"FEATURES = {FEATURES!r}\n")
        f.write(f"N_FEATURES = {len(FEATURES)}\n")
        if meta:
            f.write(f"META = {meta!r}\n")
        f.write("\n" + src + "\n")
        f.write(f"GOLDEN_X = {gx!r}\n")
        f.write(f"GOLDEN_Y = {gy!r}\n")
    return path


# ---- scalar feature codegen (single source of truth for alpha + EA) ----
# Distances normalized by Bollinger-Band WIDTH dev1.0 (bb200 for the long SMA200
# distance, bb20 for the shorter ones). ATR appears ONLY in atrfrac (#10).
_FEAT_PATTERNS = [
    'g("rsi14_raw","{tf}") - 50.0',
    'g("rsi4_raw","{tf}") - 50.0',
    'g("cci30_raw","{tf}")',
    'g("cci100_raw","{tf}")',
    'div(g("sma_p1_s0","{tf}") - g("sma_p200_s0","{tf}"), g("bb200_dev1.0_upper","{tf}") - g("bb200_dev1.0_lower","{tf}"))',
    'div(g("sma_p1_s0","{tf}") - g("sma_p50_s0","{tf}"), g("bb20_dev1.0_upper","{tf}") - g("bb20_dev1.0_lower","{tf}"))',
    'div(g("sma_p1_s0","{tf}") - g("bb20_dev1.0_middle","{tf}"), g("bb20_dev1.0_upper","{tf}") - g("bb20_dev1.0_lower","{tf}"))',
    'div(g("sma_p1_s0","{tf}") - g("bb20_dev2.0_lower","{tf}"), g("bb20_dev2.0_upper","{tf}") - g("bb20_dev2.0_lower","{tf}"))',
    'div(g("sma_p1_s0","{tf}") - g("sma_p4_s3","{tf}"), g("bb20_dev1.0_upper","{tf}") - g("bb20_dev1.0_lower","{tf}"))',
    'div(g("atr14_raw","{tf}"), g("sma_p1_s0","{tf}"))',
    'div(g("sma_p1_s0","{tf}") - g("sma4_sh4_high","{tf}"), g("bb20_dev1.0_upper","{tf}") - g("bb20_dev1.0_lower","{tf}"))',
    'div(g("sma_p1_s0","{tf}") - g("sma4_sh4_low","{tf}"), g("bb20_dev1.0_upper","{tf}") - g("bb20_dev1.0_lower","{tf}"))',
]


def scalar_feature_lines(timeframes):
    lines = ["    x = ["]
    for tf in timeframes:
        lines.append(f"        # --- {tf} ---")
        for pat in _FEAT_PATTERNS:
            lines.append("        " + pat.format(tf=tf) + ",")
    lines.append("    ]")
    return "\n".join(lines)


