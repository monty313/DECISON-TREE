# ---- MQL5 EA generation ------------------------------------------------------
_TF_ENUM = {"1m": "PERIOD_M1", "5m": "PERIOD_M5", "30m": "PERIOD_M30", "4h": "PERIOD_H4", "1d": "PERIOD_D1"}


def mql_feature_lines(timeframes):
    """MQL5 body filling x[] in FEATURES order (mirrors feature_spec). Distances
    use BB-width dev1.0: w20 = bb20 dev1 (= half the dev2 width); w200 = bb200 dev1.
    All reads at the last CLOSED bar (shift 1) + internal lags (fan +3, stacks +4)."""
    L = []
    for i, tf in enumerate(timeframes):
        b = i * 12
        L += [
            f"   // ---- {tf} ----",
            f"   double cl{i}=iClose(_Symbol,TFS[{i}],1);",
            f"   double r14_{i}=Buf(hRSI14[{i}],1), r4_{i}=Buf(hRSI4[{i}],1);",
            f"   double c30_{i}=Buf(hCCI30[{i}],1), c100_{i}=Buf(hCCI100[{i}],1);",
            f"   double s200_{i}=Buf(hSMA200[{i}],1), s50_{i}=Buf(hSMA50[{i}],1), s20_{i}=Buf(hSMA20[{i}],1);",
            f"   double s4_{i}=Buf(hSMA4c[{i}],4), atr_{i}=Buf(hATR[{i}],1);",
            f"   double bbu2_{i}=BufN(hBB20[{i}],1,1), bbl2_{i}=BufN(hBB20[{i}],2,1);",
            f"   double b2u_{i}=BufN(hBB200[{i}],1,1), b2l_{i}=BufN(hBB200[{i}],2,1);",
            f"   double sh_{i}=Buf(hSMA4h[{i}],5), slw_{i}=Buf(hSMA4l[{i}],5);",
            f"   double w20_{i}=(bbu2_{i}-bbl2_{i})/2.0, w200_{i}=(b2u_{i}-b2l_{i});",
            f"   if(cl{i}==EMPTY_VALUE||r14_{i}==EMPTY_VALUE||r4_{i}==EMPTY_VALUE||c30_{i}==EMPTY_VALUE||c100_{i}==EMPTY_VALUE||s200_{i}==EMPTY_VALUE||s50_{i}==EMPTY_VALUE||s20_{i}==EMPTY_VALUE||s4_{i}==EMPTY_VALUE||atr_{i}==EMPTY_VALUE||bbu2_{i}==EMPTY_VALUE||bbl2_{i}==EMPTY_VALUE||b2u_{i}==EMPTY_VALUE||b2l_{i}==EMPTY_VALUE||sh_{i}==EMPTY_VALUE||slw_{i}==EMPTY_VALUE) return(false);",
            f"   x[{b+0}]=r14_{i}-50.0;  x[{b+1}]=r4_{i}-50.0;  x[{b+2}]=c30_{i};  x[{b+3}]=c100_{i};",
            f"   x[{b+4}]=Div(cl{i}-s200_{i},w200_{i});  x[{b+5}]=Div(cl{i}-s50_{i},w20_{i});  x[{b+6}]=Div(cl{i}-s20_{i},w20_{i});",
            f"   x[{b+7}]=Div(cl{i}-bbl2_{i},bbu2_{i}-bbl2_{i});  x[{b+8}]=Div(cl{i}-s4_{i},w20_{i});  x[{b+9}]=Div(atr_{i},cl{i});",
            f"   x[{b+10}]=Div(cl{i}-sh_{i},w20_{i});  x[{b+11}]=Div(cl{i}-slw_{i},w20_{i});",
        ]
    return "\n".join(L)


